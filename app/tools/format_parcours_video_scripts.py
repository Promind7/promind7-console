def main() -> None:
    ap = argparse.ArgumentParser(description="Régénère les scripts phase 0 (000-Initiation, .docx).")
    ap.add_argument(
        "--out-dir",
        type=str,
        default="",
        help="Dossier de sortie (défaut : pack 000-Initiation). Utile si les .docx sont ouverts dans Word.",
    )
    args = ap.parse_args()
    if not PACK.is_dir():
        raise SystemExit(f"Dossier introuvable : {PACK}")
    out_root = Path(args.out_dir.strip()) if args.out_dir.strip() else PACK
    if args.out_dir.strip():
        out_root.mkdir(parents=True, exist_ok=True)
    builders = {
        "Leçon 1 - trailer-stage&emploi.docx": build_lesson_1,
        "Leçon 2 - questions-stage&emploi.docx": build_lesson_2,
        "Leçon 3 - orientation-domaine.docx": build_lesson_3,
        "Leçon 4 - méthode.docx": build_lesson_4,
        "Leçon 5 - Resultat.docx": build_lesson_5,
    }
    for name, fn in builders.items():
        out = out_root / name
        doc = fn()
        apply_rtl_to_document(doc)
        doc.save(str(out))
        print("OK", out)


if __name__ == "__main__":
    main()
